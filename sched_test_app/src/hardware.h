#include <unistd.h>

#ifndef HARDWARE_H
#define HARDWARE_H

static inline unsigned int get_num_processors()
{
#ifdef _SC_NPROCESSORS_ONLN
	return sysconf(_SC_NPROCESSORS_ONLN);
#else
#error Your system does not have sysconf support for _SC_NPROCESSORS_ONLN
#endif
}

#endif				/* HARDWARE_H */
